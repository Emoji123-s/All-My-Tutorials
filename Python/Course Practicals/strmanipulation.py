#A program to learn string Concatenation

print("hello world\nhello world\nhello world")

print("hello" + "divine")

#To concatenate a string, there are 3 ways
print("hello " + "divine") # Once printed, there will be no space between them
print("hello" + " divine") # Once printed, there will be a spce, because we added a space to the "divine" string before printing. You can also add a space after the "o" in "hello" to get the same result
print("hello" + " " + "divine") # Once printed, a space would be added between the strings "hello" and "divine", because of the blank space character.

